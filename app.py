@app.route('/generate', methods=['POST'])
def generate():
    """
    Обрабатывает POST-запрос на генерацию резюме.

    Принимает JSON с данными формы, валидирует,
    генерирует текст резюме и возвращает его.
    """
    data = request.get_json()

    is_valid, error_message = validate_input(data)
    if not is_valid:
        return jsonify({'text': error_message})

    name = data['name'].strip()
    vacancy = data['vacancy'].strip()
    competencies = [c.strip() for c in data['competencies'].split(',') if c.strip()]
    age = data['age']
    style = data['style']

    generators = {
        'серьезный': generate_serious_text,
        'остроумный': generate_witty_text,
        'безграмотный': generate_illiterate_text,
    }

    generator = generators.get(style, generate_serious_text)
    resume_text = generator(name, vacancy, competencies, age)
    resume_text = adjust_text_length(resume_text)

    return jsonify({'text': resume_text})


