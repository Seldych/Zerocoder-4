def count_significant_chars(text):
    """
    Подсчитывает количество значимых символов в тексте.

    Учитываются только буквы и цифры.
    Пробелы, знаки препинания и спецсимволы исключаются.

    Args:
        text (str): Исходный текст.

    Returns:
        int: Количество значимых символов.
    """
    cleaned = re.sub(r'[\W_]', '', text)
    return len(cleaned)


def validate_input(data):
    """
    Проверяет корректность входных данных формы.

    Args:
        data (dict): Словарь с полями формы.

    Returns:
        tuple: (is_valid (bool), error_message (str или None)).
    """
    name = data.get('name', '').strip()
    vacancy = data.get('vacancy', '').strip()
    competencies = data.get('competencies', '').strip()
    age = data.get('age', '')
    style = data.get('style', '')

    # Проверка заполнения всех полей
    if not all([name, vacancy, competencies, age, style]):
        return False, 'заполните все поля'

    # Проверка на осмысленность введённых данных
    fields_to_check = [
        (name, 'имя'),
        (vacancy, 'название вакансии'),
        (competencies, 'компетенции'),
    ]

    for field, field_name in fields_to_check:
        letters = [c for c in field if c.isalpha()]

        if len(letters) < 2:
            return False, 'введите осмысленную информацию'

        unique_letters = set(l.lower() for l in letters)
        if len(unique_letters) < 2 and len(field) > 2:
            return False, 'введите осмысленную информацию'

    return True, None


def generate_title(name, vacancy, style):
    """
    Генерирует заголовок резюме в зависимости от стиля.

    Args:
        name (str): Имя соискателя.
        vacancy (str): Название вакансии.
        style (str): Стиль изложения.

    Returns:
        str: Заголовок резюме.
    """
    templates = {
        'серьезный': (
            f'РЕЗЮМЕ\n'
            f'{name}\n'
            f'Соискатель на должность: {vacancy}'
        ),
        'остроумный': (
            f'Моё скромное резюме\n'
            f'Кандидат: {name}\n'
            f'Желаемая должность: {vacancy}'
        ),
        'безграмотный': (
            f'РИЗЮМЭ\n'
            f'Я: {name}\n'
            f'Кем хачу работать: {vacancy}'
        ),
    }
    return templates.get(style, templates['серьезный'])


